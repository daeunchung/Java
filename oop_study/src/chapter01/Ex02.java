package chapter01;

public class Ex02 {
	public static void main(String[] args) {
		
		int num = 10;					// num   출력화면
		System.out.println(num++);		// 10    11
		System.out.println(++num);		// 11    11
		System.out.println(num++);		// 11    12
		System.out.println(++num);		// 12    12
		System.out.println(num++);		// 12    13
		System.out.println(++num);		// 13    13
		System.out.println(num++);		// 13    14
	}
}
