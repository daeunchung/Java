package step4;
import java.util.Scanner;
/*
 * # 단어 교체하기(replace)
 * 1. text변수 문장 속에서 변경하고 싶은 단어를 입력받아,
 * 2. 교체해주는 기능을 구현한다.
 * 예)
 * 		Life is too short.
 * 		변경하고 싶은 단어입력 : Life
 * 		바꿀 단어입력 : Time
 * 
 * 		Time is too short.
 */

public class Ex09_2 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);

		String text = "Life is too short.";
		System.out.println(text);
		
		System.out.print("변경하고 싶은 단어를 입력하세요 : ");
		String word = scan.nextLine();
		
		// String text 를 char[] arr 에 한 문자씩 나눠서 답는 과정    아 주 중 요 해  핵 심 !!!!!!!!!!  //
		char[] arr = new char[text.length()];
		for(int i = 0; i<text.length(); i++) {
			arr[i] = text.charAt(i);
		}
		
		int size = word.length();
		int check = -1; // 변경하고 싶다고 입력 받은 단어가 String text에 완전히 들어있는지 체크 하는 변수
		// -1 (없다) <-> 1 (있다)
		int idx = 0;
		
		// 
		for(int i = 0; i<arr.length - size + 1; i++) {
			int count = 0;
			for(int j = 0; j<size; j++) {
				if(arr[i+j] == word.charAt(j)) {
					count ++;
				}
			}
			if(count == size) {
				idx = i; // j = size - 1 일때 
				check = 1;
			}
		}
		
		// 입력 받은 word 가 String text 안에 완전히 다 있으면
		if(check == 1) {
			int firstIdx = idx;
			int lastIdx = idx + size;
			
			int leng = lastIdx - firstIdx + 1;
			System.out.print("무엇으로 바꾸시겠습니까? ");
			
			String change = scan.nextLine();
			int length = change.length();
			
			char[] temp = arr; // arr배열을 temp배열에 옮겨놓고
			
			String front = ""; // 빈 문자열 front 를 만들어
			for(int i = 0; i<firstIdx; i++) {
				front += temp[i];
			}System.out.println(front);
			
			String back = ""; // 빈 문자열 back 을 만들어
			for(int i = lastIdx; i<temp.length; i++) {
				back += temp[i];
			}System.out.println(back);
			
			String result = front + change + back;
			System.out.println(result);
			
			
		}else {
			System.out.println("해당 단어는 존재하지 않습니다.");
		}
		
	}

}
